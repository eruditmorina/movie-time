class Movie:
    def __init__(self, name, year, director, image_url, backdrop_url, runtime, budget, revenue, overview, release_date, url):
        self.name = name
        self.year = year
        self.director = director
        self.image_url = image_url
        self.backdrop_url = backdrop_url
        self.runtime = runtime
        self.budget = budget
        self.revenue = revenue
        self.overview = overview
        self.release_date = release_date
        self.url = url